class Jmathsample1{
	public static void main(String args[]){
		int n=30;
		int a=3;
		int result;

		result=n/a;
		System.out.println(result);

		result=result*a;
		System.out.println(result);

		result=result+n;
		System.out.println(result);

		result=result-a;
		System.out.println(result);

		result=result%n;
		System.out.println(result);
	}
}